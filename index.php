                <h2>Which base oil to choose, is each suitable for our essential oil?</h2>
                <p>The first rule of aromatherapy is:</p>
                <blockquote>
                    "Essential oils are highly concentrated substances that must be diluted before being applied to the skin."
                </blockquote>
                <p>We are aware that there are general recommendations for choosing an essential oil dilution percentage, but this is not the only thing that should be considered when formulating products for skin application or internal intake of oils.</p>
                <p>An important role is played by the choice of the BASE for diluting essential oils. The base determines the percentage of transdermal penetration of essential oil components, affects the level of evaporation of essential oils from the surface of the skin. Choosing a safe percentage of the concentration of essential oils in the mixture depends on the choice of base.</p>
                <p>As we already know, when essential oils are applied to the skin, the body absorbs an average of 5-10% of the essential oils, or rather the essential oil components, as part of the base. Not all components of oils penetrate the skin equally easily. What determines whether the upper or lower limit of the specified range is reached?</p>
                <p>One of the factors is the proportion of the fat phase of the agent. Essential oils are lipophilic substances (they have an affinity for fats), they are soluble in fats and insoluble in water. Using vegetable oil as a base, we provide almost instant dissolution of essential oils in a fatty medium.</p>
                <p>If we use a water-based product as a base, for example a gel, the essential oils cannot dissolve in it. There is a peculiar distribution of essential oil particles in the gel. Our skin easily absorbs fat-soluble substances. Therefore, the components of the essential oil diluted in a gel will tend to combine with the lipids of the skin and leave the aqueous environment foreign to them much faster and to a greater extent than the components of the essential oil diluted in vegetable oil.</p>
                <p>Obviously, the greater the fraction of the fat phase of the agent used as a base for diluting essential oils, the lower the percentage of transdermal penetration of the essential oil components. The gel provides us with the maximum percentage of penetration, the emulsion a little less, and the lower limit of the range is provided by the use of an oil base.</p>
                <p>Therefore, it is recommended to reduce the concentration of essential oils to 0.5-1.0% when working with emulsions and gels for products for regular use and to 2.5% for short-term treatment of acute problems.</p>
                <p>The same can be attributed to dissolving essential oils directly in water. For example, for bathing. The main task in this case is to ensure the preliminary dissolution of the essential oils in a suitable carrier. Unfortunately, neither milk, nor honey, nor salt will ensure complete dissolution of the esters. This means that the essential oils will float on the surface of the water to a greater or lesser extent, posing a danger to our skin and mucous membranes, especially when submerged.</p>
                <p>The scenario is very similar to applying gel-based products. Under the influence of high temperatures, essential oils will literally "stick" to the skin in search of an oily environment. A safe option for diluting essential oils before adding them to the bath is to use polysorbate, a neutral hydrophilic oil, bath foam, or an organic solvent.</p>
                <p>The obvious advantage of water-based products in terms of the penetration rate of essential oils does not at all mean the uselessness of using vegetable oils as a base. Thanks to their composition, the vegetable oils themselves are a powerful therapeutic product, and the presence of essential oils contributes to a deeper penetration of the valuable components of the vegetable oils. The oil base prevents the rapid evaporation of essential oils from the surface of the skin, which occurs when using water-based products.</p>
                <p>Finally, there are situations when we don't need deep penetration of essential oils. For example, in the treatment of skin diseases.</p>
                <p>An interesting nuance is the fact that the percentage of penetration of essential oil components can increase to 70% if the area of application of the agent is covered with a film for 30-60 minutes.</p>
                <p>Regardless of the base chosen, it is important to remember the property of essential oils to improve the transdermal penetration of the associated components. For example, applying a lotion enriched with vitamin C and lemon essential oil to the skin showed a 4-fold increase in vitamin C absorption compared to a lotion without the addition of lemon essential oil.</p>
                <p>A number of studies have confirmed an increase (up to 100 times in some cases) in the absorption of drugs applied to the skin. Therefore, it is necessary to be extremely careful when applying products enriched with essential oils in close proximity to hormonal and nicotine patches.</p>
